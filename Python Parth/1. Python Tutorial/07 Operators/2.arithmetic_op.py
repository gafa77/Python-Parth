# Arithmetic Operators

#Arithmetic operators are used with numeric values to perform common mathematical operations:

"""
Operator	Name	          Example	
+	        Addition	      x + y	
-	        Subtraction	      x - y	
*	        Multiplication	  x * y	
/	        Division	      x / y	
%	        Modulus	          x % y	
**	        Exponentiation	  x ** y	
//	        Floor division	  x // y"""

x = 5
y = 3

# Addition
print(x + y)

# Subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / y)

# Modulus
print(x % y)

# Exponentiation
print(x ** y)  #same as 2*2*2*2*2


# Floor division
print(x // y)
#the floor division // rounds the result down to the nearest whole number.

